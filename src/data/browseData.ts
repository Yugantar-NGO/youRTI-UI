/**
 * Get browse page data with pagination
 */
export function getBrowsePageData(page: number = 1, resultsPerPage: number = 12): BrowsePageData {
  const startIdx = (page - 1) * resultsPerPage
  const endIdx = startIdx + resultsPerPage
  const paginatedResults = dummyRTIResults.slice(startIdx, endIdx)

  return {
    results: paginatedResults,
    filterOptions: dummyFilterOptions,
    totalCount: dummyRTIResults.length,
    currentPage: page,
    totalPages: Math.ceil(dummyRTIResults.length / resultsPerPage),
    resultsPerPage,
  }
}
